public class Fraction {
    private int a, b;
    public Fraction(int x, int y){
        //representation
        this.a = x ;
        this.b = y ;
    }
    public int somme(){
        return a ;
    }

    public int multiplication(){
        return b ;
    }

    public int difference(){
        return a;
    }

    public int division(){
        return a;
    }

    public int reduction(){
        return b ;
    }
}
